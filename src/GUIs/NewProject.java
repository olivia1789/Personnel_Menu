    private void ConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmButtonActionPerformed
        try {
            
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate startdate = LocalDate.parse(startDateField.getText(), formatter);
            LocalDate endDate = LocalDate.parse(endDateField.getText(), formatter);

            Project newProject = new Project(0, projectTitleField.getText(), startdate, endDate, Double.parseDouble(projectBudgetField.getText()));

            repository.insertNewProject(newProject);
            
            JOptionPane.showMessageDialog(null, "New project added successfully");
            MainMenu main = new MainMenu();
            main.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            System.out.println("Error in NewProject class: " + e);
            JOptionPane.showMessageDialog(null, "Error entering new project. New project not added to the database.");
        }
    }//GEN-LAST:event_ConfirmButtonActionPerformed

